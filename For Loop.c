// PENJELASAN FOR LOOP
// For loop adalah sebuah struktur perulangan yang digunakan 
// untuk mengulang eksekusi sebuah blok kode selama suatu 
// kondisi tertentu terpenuhi. For loop biasanya terdiri 
// dari tiga bagian utama: inisialisasi, kondisi, dan 
// perubahan nilai.

// IMPLEMENTASI FOR LOOP
#include <stdio.h>
int main(){
    for(int i=0; i<20; i++){
        printf("%d ",i);
    }
}

// KESULITAN FOR LOOP
// Pada for loop kondisi akhir harus ditentukan dari awal, 
// terkadang sulit untuk paham bagaimana kondisi akhir yang 
// sebenarnya dibutuhkan